        elseif ((INWVOL.eq.1).and.(NSTEP.gt.0)) then
          qsum(NUMBER(7)+4) = qsum(NUMBER(7)+4)+CHARGE
          if (cnames(NLEVEL).eq.'ACBO') then
c           print *, '*-* entering ACBO'
            qsumb(NUMBER(7)+4) = qsumb(NUMBER(7)+4)+CHARGE
          elseif (cnames(NLEVEL).eq.'PIN1') then
